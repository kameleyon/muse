// Helper function to generate snake_case IDs
const generateId = (name: string): string => {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9\s]/g, '') // Remove special chars except space
    .trim()
    .replace(/\s+/g, '_'); // Replace spaces with underscores
};

